//! Bitmap fonts for Quantum Seed Vault display
//!
//! Includes basic 6x8 ASCII font for text rendering.

const types = @import("types.zig");
const FontSize = types.FontSize;

/// Get character bitmap data
pub fn getCharBitmap(char: u8, size: FontSize) []const u8 {
    return switch (size) {
        .small => getSmallChar(char),
        .medium => getMediumChar(char),
        .large => getLargeChar(char),
    };
}

/// 6x8 font (small) - covers ASCII 32-126
fn getSmallChar(char: u8) []const u8 {
    if (char < 32 or char > 126) return FONT_6X8[0..6]; // Space for invalid
    const idx = @as(usize, char - 32) * 6;
    if (idx + 6 > FONT_6X8.len) return FONT_6X8[0..6];
    return FONT_6X8[idx..][0..6];
}

/// 8x16 font (medium) - placeholder, uses scaled small font
fn getMediumChar(char: u8) []const u8 {
    // For now, return small font data - UI will handle scaling
    return getSmallChar(char);
}

/// 16x16 font (large) - placeholder
fn getLargeChar(char: u8) []const u8 {
    return getSmallChar(char);
}

