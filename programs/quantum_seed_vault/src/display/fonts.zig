/// Calculate text width in pixels
pub fn textWidth(text: []const u8, size: FontSize) u16 {
    return @as(u16, @intCast(text.len)) * size.charWidth();
}

/// Calculate text height in pixels
pub fn textHeight(size: FontSize) u16 {
    return size.charHeight();
}
