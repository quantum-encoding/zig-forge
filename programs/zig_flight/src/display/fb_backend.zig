test "colorFromPalette mapping" {
    const black = colorFromPalette(.black);
    try std.testing.expectEqual(@as(u32, 0xFF000000), black);

    const green = colorFromPalette(.bright_green);
    try std.testing.expectEqual(@as(u32, 0xFF55FF55), green);

    const white = colorFromPalette(.bright_white);
    try std.testing.expectEqual(@as(u32, 0xFFFFFFFF), white);
}

test "font glyph lookup" {
    // Space should be all zeros
    const space = font_8x16.getGlyph(' ');
    for (space) |row| {
        try std.testing.expectEqual(@as(u8, 0), row);
    }

    // 'A' should have non-zero rows
    const a_glyph = font_8x16.getGlyph('A');
    var has_pixels = false;
    for (a_glyph) |row| {
        if (row != 0) has_pixels = true;
    }
    try std.testing.expect(has_pixels);

    // Unprintable character falls back to space
    const ctrl = font_8x16.getGlyph(0x01);
    try std.testing.expectEqual(space, ctrl);
}

