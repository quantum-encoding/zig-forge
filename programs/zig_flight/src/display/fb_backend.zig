//! Linux framebuffer backend — pixel-level rendering via /dev/fb0.
//!
//! Compile-gated: fully functional on Linux, stubs on other platforms.
//! Provides setPixel, drawLine (Bresenham), fillRect, drawRect, drawChar/drawText
//! with an embedded 8x16 VGA bitmap font. No external dependencies.

const std = @import("std");
const builtin = @import("builtin");
const tui = @import("tui_backend.zig");

const is_linux = builtin.os.tag == .linux;

// ============================================================================
// Color mapping from TUI palette to RGB32 (0xAARRGGBB)
// ============================================================================

pub fn colorFromPalette(c: tui.Color) u32 {
    return switch (c) {
        .black => 0xFF000000,
        .red => 0xFFAA0000,
        .green => 0xFF00AA00,
        .yellow => 0xFFAA5500,
        .blue => 0xFF0000AA,
        .cyan => 0xFF00AAAA,
        .white => 0xFFAAAAAA,
        .dim => 0xFF555555,
        .bright_red => 0xFFFF5555,
        .bright_green => 0xFF55FF55,
        .bright_yellow => 0xFFFFFF55,
        .bright_cyan => 0xFF55FFFF,
        .bright_white => 0xFFFFFFFF,
    };
}

// ============================================================================
// Framebuffer struct — Linux mmap'd, stub on other platforms
// ============================================================================

pub const Framebuffer = if (is_linux) LinuxFramebuffer else StubFramebuffer;

const LinuxFramebuffer = struct {
    buffer: [*]align(4096) u8 = undefined,
    width: u32 = 0,
    height: u32 = 0,
    stride: u32 = 0, // bytes per scanline
    bpp: u32 = 32,
    size: usize = 0,
    fd: i32 = -1,

    const Self = @This();

    // ioctl constants
    const FBIOGET_VSCREENINFO: c_ulong = 0x4600;
    const FBIOGET_FSCREENINFO: c_ulong = 0x4602;

    // Simplified fb_var_screeninfo — only fields we need
    const FbVarScreeninfo = extern struct {
        xres: u32,
        yres: u32,
        xres_virtual: u32,
        yres_virtual: u32,
        xoffset: u32,
        yoffset: u32,
        bits_per_pixel: u32,
        grayscale: u32,
        // rest we don't care about — pad to full struct size
        _pad: [128]u8,
    };

    // Simplified fb_fix_screeninfo — only smem_len and line_length
    const FbFixScreeninfo = extern struct {
        id: [16]u8,
        smem_start: usize,
        smem_len: u32,
        fb_type: u32,
        type_aux: u32,
        visual: u32,
        xpanstep: u16,
        ypanstep: u16,
        ywrapstep: u16,
        _pad0: u16,
        line_length: u32,
        _pad: [128]u8,
    };

    extern "c" fn ioctl(fd: c_int, request: c_ulong, ...) c_int;

    pub fn init() !Self {
        var self = Self{};

        // Open /dev/fb0
        self.fd = std.c.open("/dev/fb0", @intFromEnum(std.posix.O{ .ACCMODE = .RDWR }), 0);
        if (self.fd < 0) return error.CannotOpenFramebuffer;

        // Get variable screen info
        var vinfo: FbVarScreeninfo = undefined;
        if (ioctl(self.fd, FBIOGET_VSCREENINFO, &vinfo) < 0) {
            _ = std.c.close(self.fd);
            return error.IoctlFailed;
        }

        self.width = vinfo.xres;
        self.height = vinfo.yres;
        self.bpp = vinfo.bits_per_pixel;

        // Get fixed screen info
        var finfo: FbFixScreeninfo = undefined;
        if (ioctl(self.fd, FBIOGET_FSCREENINFO, &finfo) < 0) {
            _ = std.c.close(self.fd);
            return error.IoctlFailed;
        }

        self.stride = finfo.line_length;
        self.size = @as(usize, self.stride) * @as(usize, self.height);

        // mmap the framebuffer
        const result = std.c.mmap(
            null,
            self.size,
            std.c.PROT.READ | std.c.PROT.WRITE,
            .{ .TYPE = .SHARED },
            self.fd,
            0,
        );
        if (result == std.c.MAP_FAILED) {
            _ = std.c.close(self.fd);
            return error.MmapFailed;
        }

        self.buffer = @alignCast(@ptrCast(result));
        return self;
    }

    pub fn deinit(self: *Self) void {
        if (self.size > 0) {
            _ = std.c.munmap(@ptrCast(self.buffer), self.size);
        }
        if (self.fd >= 0) {
            _ = std.c.close(self.fd);
            self.fd = -1;
        }
    }

    pub fn setPixel(self: *Self, x: i32, y: i32, color: u32) void {
        if (x < 0 or y < 0) return;
        const ux: u32 = @intCast(x);
        const uy: u32 = @intCast(y);
        if (ux >= self.width or uy >= self.height) return;

        const offset = @as(usize, uy) * @as(usize, self.stride) + @as(usize, ux) * (self.bpp / 8);
        if (offset + 3 < self.size) {
            const ptr: *align(1) u32 = @ptrCast(self.buffer + offset);
            ptr.* = color;
        }
    }

    pub fn clear(self: *Self, color: u32) void {
        var y: u32 = 0;
        while (y < self.height) : (y += 1) {
            var x: u32 = 0;
            while (x < self.width) : (x += 1) {
                const offset = @as(usize, y) * @as(usize, self.stride) + @as(usize, x) * (self.bpp / 8);
                if (offset + 3 < self.size) {
                    const ptr: *align(1) u32 = @ptrCast(self.buffer + offset);
                    ptr.* = color;
                }
            }
        }
    }

    pub fn fillRect(self: *Self, x: i32, y: i32, w: u32, h: u32, color: u32) void {
        const start_y: i32 = @max(0, y);
        const end_y: i32 = @min(@as(i32, @intCast(self.height)), y + @as(i32, @intCast(h)));
        const start_x: i32 = @max(0, x);
        const end_x: i32 = @min(@as(i32, @intCast(self.width)), x + @as(i32, @intCast(w)));

        var cy = start_y;
        while (cy < end_y) : (cy += 1) {
            var cx = start_x;
            while (cx < end_x) : (cx += 1) {
                self.setPixel(cx, cy, color);
            }
        }
    }

    pub fn drawRect(self: *Self, x: i32, y: i32, w: u32, h: u32, color: u32) void {
        const iw: i32 = @intCast(w);
        const ih: i32 = @intCast(h);
        // Top and bottom
        self.drawHLine(x, y, w, color);
        self.drawHLine(x, y + ih - 1, w, color);
        // Left and right
        self.drawVLine(x, y, h, color);
        self.drawVLine(x + iw - 1, y, h, color);
    }

    pub fn drawHLine(self: *Self, x: i32, y: i32, len: u32, color: u32) void {
        var i: u32 = 0;
        while (i < len) : (i += 1) {
            self.setPixel(x + @as(i32, @intCast(i)), y, color);
        }
    }

    pub fn drawVLine(self: *Self, x: i32, y: i32, len: u32, color: u32) void {
        var i: u32 = 0;
        while (i < len) : (i += 1) {
            self.setPixel(x, y + @as(i32, @intCast(i)), color);
        }
    }

    /// Draw a line using Bresenham's algorithm.
    pub fn drawLine(self: *Self, x0: i32, y0: i32, x1: i32, y1: i32, color: u32) void {
        var cx = x0;
        var cy = y0;
        const dx: i32 = @intCast(@abs(x1 - x0));
        const dy: i32 = @intCast(@abs(y1 - y0));
        const sx: i32 = if (x0 < x1) 1 else -1;
        const sy: i32 = if (y0 < y1) 1 else -1;
        var err = dx - dy;

        while (true) {
            self.setPixel(cx, cy, color);
            if (cx == x1 and cy == y1) break;
            const e2 = err * 2;
            if (e2 > -dy) {
                err -= dy;
                cx += sx;
            }
            if (e2 < dx) {
                err += dx;
                cy += sy;
            }
        }
    }

    /// Draw a single character using the embedded 8x16 VGA font.
    pub fn drawChar(self: *Self, x: i32, y: i32, char: u8, fg: u32, bg: u32) void {
        const glyph = font_8x16.getGlyph(char);
        for (0..16) |row| {
            const bits = glyph[row];
            for (0..8) |col| {
                const mask: u8 = @as(u8, 0x80) >> @intCast(col);
                const color = if (bits & mask != 0) fg else bg;
                self.setPixel(x + @as(i32, @intCast(col)), y + @as(i32, @intCast(row)), color);
            }
        }
    }

    /// Draw a text string using the embedded 8x16 font.
    pub fn drawText(self: *Self, x: i32, y: i32, text: []const u8, fg: u32, bg: u32) void {
        var cx = x;
        for (text) |char| {
            self.drawChar(cx, y, char, fg, bg);
            cx += 8;
        }
    }

    /// Present — no-op for single-buffered /dev/fb0.
    pub fn present(_: *Self) void {}
};

/// Stub framebuffer for non-Linux platforms (macOS, etc.)
const StubFramebuffer = struct {
    width: u32 = 0,
    height: u32 = 0,

    pub fn init() !StubFramebuffer {
        return error.NotSupported;
    }

    pub fn deinit(_: *StubFramebuffer) void {}
    pub fn setPixel(_: *StubFramebuffer, _: i32, _: i32, _: u32) void {}
    pub fn clear(_: *StubFramebuffer, _: u32) void {}
    pub fn fillRect(_: *StubFramebuffer, _: i32, _: i32, _: u32, _: u32, _: u32) void {}
    pub fn drawRect(_: *StubFramebuffer, _: i32, _: i32, _: u32, _: u32, _: u32) void {}
    pub fn drawHLine(_: *StubFramebuffer, _: i32, _: i32, _: u32, _: u32) void {}
    pub fn drawVLine(_: *StubFramebuffer, _: i32, _: i32, _: u32, _: u32) void {}
    pub fn drawLine(_: *StubFramebuffer, _: i32, _: i32, _: i32, _: i32, _: u32) void {}
    pub fn drawChar(_: *StubFramebuffer, _: i32, _: i32, _: u8, _: u32, _: u32) void {}
    pub fn drawText(_: *StubFramebuffer, _: i32, _: i32, _: []const u8, _: u32, _: u32) void {}
    pub fn present(_: *StubFramebuffer) void {}
};

