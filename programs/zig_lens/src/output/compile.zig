test "detectLangTag for common extensions" {
    try std.testing.expectEqualSlices(u8, "zig", detectLangTag("main.zig"));
    try std.testing.expectEqualSlices(u8, "rust", detectLangTag("lib.rs"));
    try std.testing.expectEqualSlices(u8, "python", detectLangTag("app.py"));
    try std.testing.expectEqualSlices(u8, "json", detectLangTag("package.json"));
    try std.testing.expectEqualSlices(u8, "typescript", detectLangTag("app.tsx"));
    try std.testing.expectEqualSlices(u8, "bash", detectLangTag("build.sh"));
    try std.testing.expectEqualSlices(u8, "markdown", detectLangTag("README.md"));
    try std.testing.expectEqualSlices(u8, "", detectLangTag("LICENSE"));
}

test "shouldSkipFile skips binary and generated files" {
    try std.testing.expect(shouldSkipFile("image.png"));
    try std.testing.expect(shouldSkipFile("archive.zip"));
    try std.testing.expect(shouldSkipFile("package-lock.json"));
    try std.testing.expect(shouldSkipFile("app.min.js"));
    try std.testing.expect(shouldSkipFile(".DS_Store"));
    try std.testing.expect(shouldSkipFile("Cargo.lock"));
    try std.testing.expect(!shouldSkipFile("main.zig"));
    try std.testing.expect(!shouldSkipFile("README.md"));
    try std.testing.expect(!shouldSkipFile("Makefile"));
    try std.testing.expect(!shouldSkipFile("Dockerfile"));
}

test "shouldSkipPath skips excluded directories" {
    try std.testing.expect(shouldSkipPath("node_modules/express/index.js"));
    try std.testing.expect(shouldSkipPath(".git/config"));
    try std.testing.expect(shouldSkipPath("zig-cache/stuff"));
    try std.testing.expect(shouldSkipPath("src/target/debug/main"));
    try std.testing.expect(!shouldSkipPath("src/main.zig"));
    try std.testing.expect(!shouldSkipPath("lib/utils.zig"));
}
